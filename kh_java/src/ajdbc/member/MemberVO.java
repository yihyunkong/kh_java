package ajdbc.member;

public class MemberVO {
	private String mem_no = "";
	private String mem_id = "";
	private	String mem_pw = "";
	private String mem_name = "";
	private String mem_zipcode = "";
	private String mem_address = "";
	
	public String getMem_no() {return mem_no;}
	public void setMem_no(String mem_no) {this.mem_no = mem_no;}
	public String getMem_id() {return mem_id;}
	public void setMem_id(String mem_id) {this.mem_id = mem_id;}
	public String getMem_pw() {return mem_pw;}
	public void setMem_pw(String mem_pw) {this.mem_pw = mem_pw;}
	public String getMem_name() {return mem_name;}
	public void setMem_name(String mem_name) {this.mem_name = mem_name;}
	public String getMem_zipcode() {return mem_zipcode;}
	public void setMem_zipcode(String mem_zipcode) {this.mem_zipcode = mem_zipcode;}
	public String getMem_address() {return mem_address;}
	public void setMem_address(String mem_address) {this.mem_address = mem_address;}
	

	
	
}
